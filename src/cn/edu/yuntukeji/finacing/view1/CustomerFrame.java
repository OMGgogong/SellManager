    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
   
		Map<String, Object> request = new HashMap<>();
		request.put("url", "Customer/save");
		

		Map<String,Object> salersMan  = new HashMap<>();
		salersMan.put("id", Integer.parseInt(txtCustomerID.getText()));
		salersMan.put("customerSimpleName", txtCompanySimpleName.getText());
		salersMan.put("customerName", txtCompanyName.getText());
		salersMan.put("owner", txtOwner.getText());
		salersMan.put("title", comboTitle.getSelectedItem());
		
		Map<String,Object> salersManId  = new HashMap<>();
		salersManId.put("salesMamId", comboSalesMan.getSelectedItem());
		
		salersMan.put("salersManId",salersManId);
		salersMan.put("mobilephone", txtMobilePhone.getText());
		salersMan.put("telephone", txtTel.getText());
		salersMan.put("fax", txtFax.getText());
		salersMan.put("cutomerAddress", txtCustomerAddress.getText());
		salersMan.put("invoiceAddress", txtInvoiceAddress.getText());
		salersMan.put("deliveryAddress", txtDeliveryAddress.getText());
		
		request.put("data", salersMan);
		
		dp.execute(request);
		sysnView();

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
    	
    	
    	Map<String, Object> request = new HashMap<>();
		request.put("url", "Customer/findById/"+tblCustomer.getValueAt(tblCustomer.getSelectedRow(),0));	
		Map<String, Object> reMap = dp.execute(request);
		JSONObject json = JSONObject.fromObject(reMap.get("result"));
		

    	
    	
    	
    	txtCustomerID.setText(json.getString("id"));
    	txtCompanySimpleName.setText(json.getString("customerSimpleName"));
    	txtCompanyName.setText(json.getString("customerName"));
    	txtOwner.setText(json.getString("owner"));
    	
 
    	if(json.getString("title").equals("先生")){
    		comboTitle.setSelectedIndex(0);
    	}else{
    		comboTitle.setSelectedIndex(1);
    	}
    	
    	txtTel.setText(json.getString("telephone"));
    	txtMobilePhone.setText(json.getString("mobilephone"));
    	txtFax.setText(json.getString("fax"));
		comboSalesMan.setSelectedItem(json.getJSONObject("salersManId").getString("salesMamId"));
    	txtCustomerAddress.setText(json.getString("cutomerAddress"));
    	txtDeliveryAddress.setText(json.getString("deliveryAddress"));
    	txtInvoiceAddress.setText(json.getString("invoiceAddress"));

    	
    	
    }//GEN-LAST:event_btnUpdateActionPerformed

