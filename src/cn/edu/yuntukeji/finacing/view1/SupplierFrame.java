    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
    	Map<String, Object> request = new HashMap<>();
		request.put("url", "Supplier/save");
	
		Map<String,Object> salersMan  = new HashMap<>();
		salersMan.put("supplierId", Integer.parseInt((String)txtSupplierID.getText()));
		salersMan.put("supplierSimpleName", txtSupplierSimpleName.getText());
		salersMan.put("supplierName", txtSupplierName.getText());
		salersMan.put("owner", txtOwner.getText());
		salersMan.put("tittle", comboTitle.getSelectedItem().toString());
		salersMan.put("telePhone", txtTel.getText());
		salersMan.put("mobilePhone", txtMobilePhone.getText());
		salersMan.put("fax", txtFax.getText());
		salersMan.put("companyAddress", txtCompanyAddress.getText());
		salersMan.put("factoryAddress", txtFactoryAddress.getText());
		request.put("data", salersMan);
		
		dp.execute(request);
		sysnView();
    }//GEN-LAST:event_btnAddActionPerformed

