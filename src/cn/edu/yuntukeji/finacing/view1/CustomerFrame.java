    private Object [][] findAllDate(){

    	Map<String, Object> request = new HashMap<>();
		request.put("url", "Customer/findAll");	
		Map<String, Object> reMap = dp.execute(request);
		JSONArray json = JSONArray.fromObject(reMap.get("result"));
		System.out.println(json);
		
		Object [][] o = new Object [json.size()][12];
		for (int i = 0;i<json.size();i++) {
			
			o[i][0] = ((JSONObject)json.get(i)).getString("id");
			o[i][1] = ((JSONObject)json.get(i)).getString("customerSimpleName");
			o[i][2] = ((JSONObject)json.get(i)).getString("customerName");
			o[i][3] = ((JSONObject)json.get(i)).getString("owner");
			o[i][4]= ((JSONObject)json.get(i)).getString("title");
			o[i][5] = ((JSONObject)json.get(i)).getString("telephone");
			o[i][6] =((JSONObject)json.get(i)).getString("mobilephone");
			o[i][7] =((JSONObject)json.get(i)).getString("fax");
			o[i][8] =((JSONObject)json.get(i)).getString("salersManId");
			o[i][9] =((JSONObject)json.get(i)).getString("cutomerAddress");
			o[i][10] =((JSONObject)json.get(i)).getString("deliveryAddress");
			o[i][11] =((JSONObject)json.get(i)).getString("invoiceAddress");
		}
		
		return o;
    }
    
