    private static Object[][] findAllDate() {
		
    	
    	Map<String, Object> request = new HashMap<>();
    	request.put("url", "DeliveryMaster/queryGodownProduct");	
		Map<String, Object> reMap = dp.execute(request);
		JSONArray json = JSONArray.fromObject(reMap.get("result"));
		System.out.println(json);
		
		Object [][] o = new Object [json.size()][6];
		for (int i = 0;i<json.size();i++) {
			// "销售退货单", "销售员", "销售日期", "客户", "客户地址", "总金额"
			o[i][0] = ((JSONObject)json.get(i)).getString("deliveryId");
			JSONObject deliveryDate =((JSONObject)json.get(i)).getJSONObject("deliveryDate");
			if(!deliveryDate.isEmpty()){
				o[i][2] = deliveryDate.getString("year")+"-"+deliveryDate.getString("month")+"-"+deliveryDate.getString("day");
				//o[i][5] =deliveryDate.getString("year")+"-"+deliveryDate.getString("month")+"-"+deliveryDate.getString("day");
			}
			
			o[i][1]= ((JSONObject)json.get(i)).getJSONObject("salesManId").getString("chineseName");
			o[i][3]= ((JSONObject)json.get(i)).getJSONObject("customerId").getString("customerName");
			o[i][4]= ((JSONObject)json.get(i)).getJSONObject("customerId").getString("cutomerAddress");
			o[i][5] = ((JSONObject)json.get(i)).getString("subTotal");
			
	
			}
		
		return o;
		
	}

