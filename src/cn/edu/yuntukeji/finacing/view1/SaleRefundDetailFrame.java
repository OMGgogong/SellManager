	private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnSaveActionPerformed
		Map<String, Object> request1 = new HashMap<>();
		request1.put("url", "DeliveryMaster/save");

		Map<String, Object> date1 = new HashMap<>();
		if (!txtDeliveryRefundID.getText().equals("")) {
			date1.put("deliveryId",
					Integer.parseInt(txtDeliveryRefundID.getText()));

			Map<String, Object> salesManId = new HashMap<>();
			salesManId.put("salesMamId", comboSalesMan.getSelectedItem());

			Map<String, Object> customerId = new HashMap<>();

			customerId.put("id", comboCustomer.getSelectedItem());

			date1.put("salesManId", salesManId);
			date1.put("customerId", customerId);

			date1.put("deliveryAddress", txtCustomerAddress.getText());
			SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
			try {
				date1.put("deliveryDate", sdf.parse(xyRefundDate.getText())
						.getTime());
			} catch (ParseException e) {
				e.printStackTrace();
			}
			date1.put("subTotal", Float.parseFloat(lblSum.getText()));

			date1.put("deliveryProperty", -1);
			request1.put("data", date1);

			dp.execute(request1);

			// 爆粗子表

			TableModel model = tblSaleRefundDetail.getModel();

			for (int i = 0; i < model.getRowCount(); i++) {

				if (txtDeliveryRefundID.getText() != null
						&& tblSaleRefundDetail.getValueAt(i, 2) != null
						&& tblSaleRefundDetail.getValueAt(i, 3) != null) {
					Map<String, Object> request = new HashMap<>();
					request.put("url", "DeliveryDetail/save");
					Map<String, Object> date = new HashMap<>();
					Map<String, Object> dm = new HashMap<>();
					dm.put("deliveryId",
							Integer.parseInt(txtDeliveryRefundID.getText()));
					date.put("deliveryId", dm);
					Map<String, Object> product = new HashMap<>();
					product.put("productId",
							tblSaleRefundDetail.getValueAt(i, 0));
					product.put("productName",
							tblSaleRefundDetail.getValueAt(i, 1));
					SimpleDateFormat sdf1 =   new SimpleDateFormat("yyyy-MM-dd");
					try {
					product.put("lastDeliveryDate", sdf1.parse(xyRefundDate.getText()).getTime());
					} catch (ParseException e) {
						e.printStackTrace();
					}
					date.put("productId", product);
					Float price = Float.parseFloat((String) tblSaleRefundDetail
							.getValueAt(i, 2));
					Float quantity = Float
							.parseFloat((String) tblSaleRefundDetail
									.getValueAt(i, 3));
					date.put("salesUnitPrice", price);
					date.put("salesQuantity", quantity);
					date.put("salesAmount", price * quantity);
					request.put("data", date);

					dp.execute(request);
				}

			}

		}

		SaleRefundFrame.sysnView();
		dispose();

	}// GEN-LAST:event_btnSaveActionPerformed

