	private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnDelActionPerformed

		Float total = 0.0f;
		for (int i = 0; i < tblSaleRefundDetail.getRowCount(); i++) {
			if (tblSaleRefundDetail.getValueAt(i, 4) != null) {
				total += (Float) tblSaleRefundDetail.getValueAt(i, 4);
			}
		}
		lblSum.setText("" + total);
		DefaultTableModel model = (DefaultTableModel) tblSaleRefundDetail
				.getModel();
		if (tblSaleRefundDetail.getSelectedRow() != -1) {
			model.removeRow(tblSaleRefundDetail.getSelectedRow());

			tblSaleRefundDetail.updateUI();
		}

	}// GEN-LAST:event_btnDelActionPerformed

