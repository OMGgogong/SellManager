private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {  
    	
    	Map<String, Object> request = new HashMap<>();
		request.put("url", "DeliveryDetail/findDeliveryDetailBydeliveryId");	
		Map<String,Object> salersMan  = new HashMap<>();
		salersMan.put("deliveryId", Integer.parseInt((String) tblSaleRefund.getValueAt(tblSaleRefund.getSelectedRow(),0)));
		request.put("param", salersMan);
		Map<String, Object> reMap = dp.execute(request);
		JSONArray json = JSONArray.fromObject(reMap.get("result"));
		System.out.println(json);
		Object [][] traderInfo= new Object[json.size()][5];
		for (int i = 0; i < json.size(); i++) {
					JSONObject	productId = ((JSONObject)json.get(i)).getJSONObject("productId");
					if(!productId.isEmpty()){
						traderInfo[i][0] = productId.getString("productId");
						traderInfo[i][1] = productId.getString("productName");
					}
					traderInfo[i][2] = ((JSONObject)json.get(i)).getString("salesUnitPrice");
					traderInfo[i][3] = ((JSONObject)json.get(i)).getString("salesQuantity");
					traderInfo[i][4] = ((JSONObject)json.get(i)).getString("salesAmount");
		}
		tblSaleDetail.setModel(new javax.swing.table.DefaultTableModel(
				 traderInfo,
		            new String [] {
		                "商品编号", "商品名称", "单价", "数量", "金额"
		            }
		        ));
		tblSaleDetail.updateUI();
    }
