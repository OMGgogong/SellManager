    private Object [][] findAllDate(){

    	Map<String, Object> request = new HashMap<>();
		request.put("url", "Supplier/findAll");	
		Map<String, Object> reMap = dp.execute(request);
		JSONArray json = JSONArray.fromObject(reMap.get("result"));
		System.out.println(json);
		
		Object [][] o = new Object [json.size()][10];
		for (int i = 0;i<json.size();i++) {
			
			o[i][0] = ((JSONObject)json.get(i)).getString("supplierId");
			o[i][1] = ((JSONObject)json.get(i)).getString("supplierSimpleName");
			o[i][2] = ((JSONObject)json.get(i)).getString("supplierName");
			o[i][3] = ((JSONObject)json.get(i)).getString("owner");
			o[i][4]= ((JSONObject)json.get(i)).getString("tittle");
			o[i][5] = ((JSONObject)json.get(i)).getString("telePhone");
			o[i][6] =((JSONObject)json.get(i)).getString("mobilePhone");
			o[i][7] =((JSONObject)json.get(i)).getString("fax");
			o[i][8] =((JSONObject)json.get(i)).getString("companyAddress");
			o[i][9] =((JSONObject)json.get(i)).getString("factoryAddress");
			
		}
		
		return o;
    }
    
